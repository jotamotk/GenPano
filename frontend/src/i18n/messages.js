/**
 * Minimal ICU-lite formatter: replaces {key} tokens.
 * Keeps the same call shape as next-intl's t(key, { params }) so the
 * migration is a search-and-replace rather than a rewrite.
 */
export function formatMessage(template, params) {
  if (!template) return '';
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, k) => (params[k] !== undefined ? String(params[k]) : `{${k}}`));
}

/** Resolve a dotted path like "reports.tabs.all" */
export function resolveKey(dict, key) {
  return key.split('.').reduce((acc, part) => (acc && acc[part] !== undefined ? acc[part] : undefined), dict);
}
